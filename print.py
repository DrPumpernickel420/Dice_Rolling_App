def return_dices(rolls):
    dice = [
        ["┌─────────┐","│         │","│    ●    │","│         │","└─────────┘"],
        ["┌─────────┐","│  ●      │","│         │","│      ●  │","└─────────┘"],
        ["┌─────────┐","│  ●      │","│    ●    │","│      ●  │","└─────────┘"],
        ["┌─────────┐","│  ●   ●  │","│         │","│  ●   ●  │","└─────────┘"],
        ["┌─────────┐","│  ●   ●  │","│    ●    │","│  ●   ●  │","└─────────┘"],
        ["┌─────────┐","│  ●   ●  │","│  ●   ●  │","│  ●   ●  │","└─────────┘"],
    ]
    result = []
    dices = [dice[x-1] for x in rolls]
    for x in range(0,5):
        result.append("   ".join([dice[x] for dice in dices]))
    return "\n".join(result)